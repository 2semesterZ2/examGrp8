                <h2 class="article_heading">Companies like Google, Facebook or Apple, can tell whether you hold negative unconsciuous attitudes against other races, women etc.</h2>
              </div>
                
              <a href="../img/article_pics/google_london.jpg">
              <img class="img-responsive article-image center-block" src="../img/article_pics/google_london.jpg"></a>
          
              <p>It is called Implicit Association Test or IAT and was invented by the psychology Proffesor Anthony Greenwald at University of Washington. People, who undergo this test, might receive somewhat bizzare results indicating that they, for example, are negatively biased towards women at work, etnic minorities or LGBT people, even though, they had never pondered holding those attitudes themselves.</p>
              
              <p>According to Forbes reporter, Ellen Huet, Google was the first-mover to set the trend for other hi-tech companies to implement IAT to their agendas. In May 30,  2014, Google revealed its diversity data to the public, showing the numbers of its workforce in regard to gender and racial makeup. It‘s not surprising to see that Hispanics ad Afro-Americans are cabined in the margins of total workforce, meanwhile women constitute only one-third of the whole. The majority of employees are represesented by white and Asian men.</p>
              
              <p>Basically, IAT is tapping into the realm of implicit congnition, trying to uncover unconscious attitudes and prejudice. Examples of implicit cognition can be: ridding the bike, juggling, skating, swimming etc. Once you learn these skills, it become automatized and you don‘t need any conscious awareness in performing these tasks. The same goes with implicit prejudice. Your culture determines how you look towards different social, ethnical or racial groups, most of the times, without any explicit awareness. An unusual handshake, a foreigner mispronouncing vowels of your own language or a picture in a job application that you read, might elicit effortless associations in your head, which you sometimes accept for granted.</p>

