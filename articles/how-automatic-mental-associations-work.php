              <p>Mahzarin Banaji and Anthony Greenwald give this example:</p>
              <blockquote>
                <p>A father and his son are in a car accident. The father dies at the scene and the son, badly injured, is rushed to the hospital. In the operating room, the surgeon looks at the boy and says, “I can’t operate on this boy. He is my son.”</p>
                </blockquote>
              <p>If for a few seconds you got surprized and the surgeon-male association popped up in your head, it means you are the hostage of the cultural influence, where surgeon’s profession is strongly related with the word “male”.</p>
              <p>Think of the association process more thoroughly. Did you have to use any volition to associate the words surgeon and male with each other or was it more like effortless and spontaneous act? If so, the notion of automatic associations suggests that it works beyond the realm of your conscious awareness.</p>
              <p>Moreover, there’s another catch in this story. Banaji and Greenwald continues by saying that:</p>
              <blockquote>
                <p>The conscious feeling of puzzlement on reading He is my son in the surgeon story is a good sign that the male = surgeon association was automatically activated, lurking outside of conscious awareness. But we must now confess that we didn’t rely on just that stereotype to set this mindbug in motion. If you count the male and female words in the story you will find a lopsided victory for male—nine male words, zero female words. The male words, in order, were: father, his, son, father, son, boy, boy, he, son. These multiple triggers to think “male” almost certainly added to activation of the surgeon = male association. Had the story instead been about an injured daughter, six of those nine words would have been female, ending with, “I can’t operate on this girl. She is my daughter.” Written that way, it might have entered your mind more rapidly that the surgeon was the mother. These extra triggers in the riddle’s wording provided a context that assisted the automatic inference of male...</p>
                </blockquote>
          
