                <h2 class="article_heading">There might be a difference whether your job application is delivered in print or digitally</h2>
              </div>
                
              <a href="../img/article_pics/job_interview.jpg">
              <img class="img-responsive article-image center-block" src="../img/article_pics/job_interview.jpg"></a>
          
              <p>Three psychology researchers from USA set themselves out to determine whether touching objects of different weights, surfaces and shapes can influence peoples’ judgements and decision-making.</p>
              <p>In one of their experiments people were asked: “to study a job candidate by looking at a resume placed on a clipboard. Each subject received either a light clipboard or a heavy one. The people given the heavy clipboards judged the applicants to have a more serious interest in the position than did the group that received the light clipboard.”</p>
              <p>According to the researchers, touching sensations can influence: “higher social cognitive processing in dimension-specific and metaphor-specific ways.”</p>
              <p>Having in mind that job applications viewed on screen are weightless, it has smaller effect for the viewer, since there is no sensational input. Meanwhile job applications in print have their weight and sense of touch, therefore are more sensationally and emotionally related. </p>
          
